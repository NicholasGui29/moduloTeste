

let somadora = (numero1, numero2) => {
    return numero1 + numero2
}


let substracao = (numero1, numero2) => {
    return numero1 - numero2
}



let multiplicacao = (numero1, numero2) => {
    return numero1 * numero2
}


let divisao = (numero1, numero2) => {
    return numero1 / numero2
}

export default somadora; substracao; multiplicacao; divisao

